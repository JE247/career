function openService() {
	var popupWidth = 800;
	var popupHeight = 600;
	var popupX = (window.screen.width / 2) - (popupWidth / 2);
	var popupY = (window.screen.height / 2) - (popupHeight / 2);
	var url = "/career/footerService";
	var name = "Career";
	var option = "width = 800px, height = 600px, top = " + popupY + ", left = " + popupX + ", resizable=no, location = no";

	window.open(url, name, option);
}

